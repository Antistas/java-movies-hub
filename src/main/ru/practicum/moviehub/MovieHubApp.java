package ru.practicum.moviehub;

import ru.practicum.moviehub.http.MoviesServer;
import ru.practicum.moviehub.store.MoviesStore;

/*
1) Сервер запускается на localhost:8080.
2) Формат запросов и ответов — JSON, кодировка UTF-8.
3) Ответы содержат корректные коды статусов и заголовки (как минимум Content-Type).
4) Данные хранятся в памяти (например, в HashMap).

Функциональные требования
Эндпоинты:
2) Добавление фильма
    Эндпоинт — POST /movies.
    Тело запроса, содержит поля:
    title — не пустая строка, длина ≤ 100 символов.
    year — число от 1888 (год самого раннего из сохранившихся фильмов) до текущий год + 1.
    Ответ:
    Если фильм успешно добавлен:
    Код статуса — 201 Created.
    Тело — JSON созданного фильма с присвоенным ID.
    Если произошла ошибка валидации:
    Код статуса — 422 Unprocessable Entity.
    Тело — объект с информацией об ошибке, содержит следующие поля:
    error — короткое описание ошибки, например, Ошибка валидации.
    details — массив строк с деталями проблемы, например:
    ["название не должно быть пустым", "год должен быть между 1888 и 2026"]
    Если был получен запрос с неправильным значением заголовка Content-Type:
    Код статуса — 415 Unsupported Media Type.
4) Удаление фильма
    Эндпоинт — DELETE /movies/{id}.
    Если удалён:
    Код статуса — 204 No Content.
    Если фильм не найден:
    Код статуса — 404 Not Found.

Нефункциональные требования
1) Чёткая структура кода (разделение моделей, хранилища, сервера).
2) Обработка ошибок с корректными кодами ответа.
3) Тесты на все эндпоинты (успешные и негативные сценарии).

Список проверок
2) POST /movies
    добавляет фильм при корректных данных;
    возвращает ошибку при пустом title;
    возвращает ошибку при слишком длинном title (> 100 символов);
    возвращает ошибку при неверном year (меньше 1888 или больше текущего года + 1);
    возвращает ошибку при неправильном Content-Type;
    возвращает ошибку при некорректном JSON.
4) DELETE /movies/{id}
    удаляет фильм по существующему id;
    возвращает ошибку, если фильм не найден;
    возвращает ошибку, если id не число.
5) GET /movies?year=YYYY
    возвращает фильмы указанного года;
    возвращает пустой список, если фильмов с таким годом нет;
    возвращает ошибку, если параметр year не число.

Общие
1) все успешные ответы содержат Content-Type — application/json; charset=UTF-8;
2) ошибки возвращают объект с полем error (и при необходимости details);
3) при неподдерживаемом HTTP-методе возвращается 405 Method Not Allowed.
 */

public class MovieHubApp {
    public static void main(String[] args) {

        MoviesStore ms = new MoviesStore();
        ms.create("Название 1", 1991);
        ms.create("Название 2", 1994);
        ms.create("Название 3", 1995);
        ms.create("Название 4", 1996);
        ms.create("Название 5", 1997);
        ms.create("Название 6", 1998);
        ms.create("Название 6555", 1995);

        final MoviesServer server = new MoviesServer(ms, 8080);
        Runtime.getRuntime().addShutdownHook(new Thread(server::stop));
        server.start();
    }
}